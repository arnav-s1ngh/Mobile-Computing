import numpy as np
import matplotlib.pyplot as plt
from sklearn.linear_model import LinearRegression
x=[1.534]*1000
y=[9.689]*1000
z=[0]*1000
time_1=list(range(1000))
time_2=list(range(100))
time_3=list(range(10))
time_1s=np.array(time_1).reshape(-1, 1)
time_10s=np.array(time_2).reshape(-1, 1)
time_20s=np.array(time_3).reshape(-1, 1)
modelx_1=LinearRegression().fit(time_1s, x)
modelx_2=LinearRegression().fit(time_10s, x[0:100])
modelx_3=LinearRegression().fit(time_20s, x[0:50])
modely_1=LinearRegression().fit(time_1s, y)
modely_2=LinearRegression().fit(time_10s, y[0:100])
modely_3=LinearRegression().fit(time_20s, y[0:50])
modelz_1=LinearRegression().fit(time_1s, z)
modelz_2=LinearRegression().fit(time_10s, z[0:100])
modelz_3=LinearRegression().fit(time_20s, z[0:50])
future_time_1s=np.array(range(1000, 1020)).reshape(-1, 1)
future_time_10s=np.array(range(100, 120)).reshape(-1, 1)
future_time_20s=np.array(range(10, 30)).reshape(-1, 1)
future_x_1=modelx_1.predict(future_time_1s)
future_x_2=modelx_2.predict(future_time_10s)
future_x_3=modelx_3.predict(future_time_20s)
future_y_1=modely_1.predict(future_time_1s)
future_y_2=modely_2.predict(future_time_10s)
future_y_3=modely_3.predict(future_time_20s)
future_z_1=modelz_1.predict(future_time_1s)
future_z_2=modelz_2.predict(future_time_10s)
future_z_3=modelz_3.predict(future_time_20s)
print('X-1', future_x_1)
print('X-10', future_x_2)
print('X-20', future_x_3)
print('Y-1', future_y_1)
print('Y-10', future_y_2)
print('Y-20', future_y_3)
print('Z-1', future_z_1)
print('Z-10', future_z_2)
print('Z-20', future_z_3)
plt.figure(figsize=(12, 18))
plt.subplot(3,3,1)
plt.plot(time_1 + list(range(1000,1020)),x+list(future_x_1))
plt.subplot(3,3,2)
plt.plot(time_2 + list(range(100,120)),x[0:100]+list(future_x_2))
plt.subplot(3,3,3)
plt.plot(time_3 + list(range(10,30)), x[0:50]+list(future_x_3))
plt.subplot(3,3,4)
plt.plot(time_1+list(range(1000,1020)),y+list(future_y_1))
plt.subplot(3,3,5)
plt.plot(time_2+list(range(100,120)),y[0:100]+list(future_y_2))
plt.subplot(3,3,6)
plt.plot(time_3+list(range(10,30)),y[0:50]+list(future_y_3))
plt.subplot(3,3,7)
plt.plot(time_1+list(range(1000,1020)),z+list(future_z_1))
plt.subplot(3,3,8)
plt.plot(time_2+list(range(100,120)),z[0:100]+list(future_z_2))
plt.subplot(3,3,9)
plt.plot(time_3+list(range(10,30)),z[0:50]+list(future_z_3))
plt.tight_layout()
plt.show()
